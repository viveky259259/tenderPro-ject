	<h3 style="color:brown">Frequently Asked Questions</h3>

<h4 style="color:green;"> What is Electronic Tendering?</h4>

<pre>Electronic Tendering is carrying out the traditional tendering process in an electronic form, using the internet.<br>Using E-Tendering the government of India can:

Raise Indents as per the requirement
Approve indents online
Sell Tenders
Receive Bids
Award Contract / PO
Evaluate Tenders
Create and publish NIT.
Includes a variety of off techniques such as RFPs, quotes, auctions and reverse auctions.</pre>

<h4 style="color:green;"> Using E-Tendering, the suppliers can.</h4>

<pre>Receive notification of the relevant tenders
Purchase tenders document
Submit Bids Online
Track the status of their bids
</pre>

<h4 style="color:green;"> What are the sources of Tenders?</h4>



<pre>
The tenders are published by the various departments of the Government of India. Using the E-Tendering System,<br>the users of the participating domain will:
Raise indents as per the requirements
Approve Indents online
Create Tender
Approve Tender and
Publish Tender online
</pre>

<h4 style="color:green;">How Secure is it?</h4>

<pre>
The security features incorporated in the application ensures that all activities are logged,<br>no unauthorized person has access to data, all sensitive data is encrypted and system can be restoring in a minimal possible <br>time in case of a disaster or system crash.
The mechanism used are:
<b>OTP (One Time Password):</b> The most important advantage that is addressed by OTPs is that, in contrast to static passwords,<br>they are not vulnerable to replay attacks. <br>It is to verify that the person who logged in using the username and password is the same who one who obtained the OTP<br>declares the person to be the authenticated one.

<b>Captcha</b>

("Completely Automated Public Turing test to tell Computer and Humans Apart"): <br>This form of CAPTCHA requires that the user type the letters of a distorted image, <br>sometimes with the addition of an obscured sequence of letters or digits that appears on the screen.<br>Since the test is administered by a computer, in contrast to the standard<br>Turing test that is administered by a human, a CAPTCHA is sometimes described as a reverse Turing test.
</pre>

